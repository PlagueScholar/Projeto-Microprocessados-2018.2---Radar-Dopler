uint32_t pulseTime(){

	  uint32_t us;

	  HAL_GPIO_WritePin(GPIOF,SR04_TRIG,GPIO_PIN_RESET);
	  __HAL_TIM_SET_COUNTER(&htim16,0);
	  while(__HAL_TIM_GET_COUNTER(&htim16)<2);
	  HAL_GPIO_WritePin(GPIOF,SR04_TRIG,GPIO_PIN_SET);
	  __HAL_TIM_SET_COUNTER(&htim16,0);
	  while(__HAL_TIM_GET_COUNTER(&htim16)<20);
	  HAL_GPIO_WritePin(GPIOF,SR04_TRIG,GPIO_PIN_RESET);
	  while(HAL_GPIO_ReadPin(GPIOF,SR04_ECHO)==0);
	  __HAL_TIM_SET_COUNTER(&htim16,0);
	  while(HAL_GPIO_ReadPin(GPIOF,SR04_ECHO)==1);
	  us = __HAL_TIM_GET_COUNTER(&htim16);
	  if((us<2) || (us>50000)){
		  return 0;
	  }
	  else return  us;
	}

int GetSpeed(void){

	int d1,d2,t2,t1;

		__HAL_TIM_SET_COUNTER(&htim17,0);
		d1 = pulseTime()*0.1754;
		t1 = __HAL_TIM_GET_COUNTER(&htim17);
		HAL_Delay(50);
		d2 = pulseTime()*0.1754;
		t2 = __HAL_TIM_GET_COUNTER(&htim17);
		return (d2-d1)/(t2-t1);
}
